package tnak;

public class Game {
	private int w,h;

	public int getH() {
		return h;
	}

	public void setH(int h) {
		this.h = h;
	}

	public int getW() {
		return w;
	}

	public void setW(int w) {
		this.w = w;
	}

	public Game(int w, int h) {
		super();
		this.h = h;
		this.w = w;
	}
	

}
